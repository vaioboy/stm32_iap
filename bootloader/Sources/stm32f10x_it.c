/**
  * @brief  Receive UART data and set flag.
  * @param  None
  * @retval None
  */
void DEBUG_UART_IRQ_HANDLER(void)
{
	if(USART_GetITStatus(DEBUG_UART, USART_IT_RXNE))
	{
		USART_ClearITPendingBit(DEBUG_UART, USART_IT_RXNE);
		
		long_array[cnt++] = USART_ReceiveData(DEBUG_UART);
		flag_data = 1;
	}
}

/**
  * @brief  Toggle LED D5 when timer interrupt.
  * @param  None
  * @retval None
  */
void LED_TIM_IRQ_HANDLER(void)
{
	if(TIM_GetITStatus(LED_TIM, TIM_IT_Update))
	{
		TIM_ClearITPendingBit(LED_TIM, TIM_IT_Update);
		
		LED_D5_TOGGLE;
	}
}

